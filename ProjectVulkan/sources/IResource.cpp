    Application::Get().GetVulkan()->CreateBuffer(bufferSize, VK_BUFFER_USAGE_TRANSFER_DST_BIT | VK_BUFFER_USAGE_INDEX_BUFFER_BIT, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, indexBuffer, indexBufferMemory);

    Application::Get().GetVulkan()->CopyBuffer(stagingBuffer, indexBuffer, bufferSize);

